export function isFormatStaple(cardName: string): boolean {
  const name = cardName.toLowerCase();
  const allStaples = [
    ...FORMAT_STAPLES.colorless,
    ...FORMAT_STAPLES.W,
    ...FORMAT_STAPLES.U,
    ...FORMAT_STAPLES.B,
    ...FORMAT_STAPLES.R,
    ...FORMAT_STAPLES.G,
  ];
  return allStaples.some((staple) => staple.toLowerCase() === name);
}

export function isArchetypeStaple(cardName: string, archetype: string): boolean {
  const name = cardName.toLowerCase();
  const staples = ARCHETYPE_STAPLES[archetype as keyof typeof ARCHETYPE_STAPLES] || [];
  return staples.some((staple) => staple.toLowerCase() === name);
}
